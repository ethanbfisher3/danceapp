// Helper function to get all moves for a dance style (flattened from all levels)
export const getAllMovesForStyle = (danceStyle, includeEndings = true) => {
  const styleKey = danceStyle?.toLowerCase().replace(/\s+/g, "_")
  const styleData = DANCE_MOVES[styleKey]

  if (!styleData) return []

  // If it's the old array format (backwards compatibility)
  if (Array.isArray(styleData)) {
    return styleData
  }

  // If it's the new object format with levels
  const allMoves = [
    ...(styleData.bronze || []),
    ...(styleData.silver || []),
    ...(styleData.gold || []),
    ...(styleData.other || []),
  ]

  // Filter out ending moves unless explicitly requested
  if (!includeEndings) {
    return allMoves.filter((m) => {
      if (typeof m === "object" && m.isEnding) {
        return false
      }
      return true
    })
  }

  return allMoves
}

// Helper function to get moves organized by level for section display
export const getMovesWithSections = (danceStyleOrStyles) => {
  // Handle both single style string and array of styles
  const styles = Array.isArray(danceStyleOrStyles)
    ? danceStyleOrStyles
    : [danceStyleOrStyles]

  // Aggregate moves from all styles
  const allSections = { bronze: [], silver: [], gold: [], other: [] }

  styles.forEach((danceStyle) => {
    const styleKey = danceStyle?.toLowerCase().replace(/\s+/g, "_")
    const styleData = DANCE_MOVES[styleKey]

    if (!styleData) return

    // If it's the old array format (backwards compatibility)
    if (Array.isArray(styleData)) {
      allSections.other.push(...styleData)
      return
    }

    // If it's the new object format with levels
    if (styleData.bronze) allSections.bronze.push(...styleData.bronze)
    if (styleData.silver) allSections.silver.push(...styleData.silver)
    if (styleData.gold) allSections.gold.push(...styleData.gold)
    if (styleData.other) allSections.other.push(...styleData.other)
  })

  // Filter out ending moves from all levels
  Object.keys(allSections).forEach((level) => {
    allSections[level] = allSections[level].filter((m) => {
      if (typeof m === "object" && m.isEnding) {
        return false
      }
      return true
    })
  })

  // Convert to sections array, filtering out empty sections
  const sections = []

  if (allSections.bronze.length > 0) {
    sections.push({ title: "Bronze", data: allSections.bronze })
  }
  if (allSections.silver.length > 0) {
    sections.push({ title: "Silver", data: allSections.silver })
  }
  if (allSections.gold.length > 0) {
    sections.push({ title: "Gold", data: allSections.gold })
  }
  if (allSections.other.length > 0) {
    sections.push({ title: "Other", data: allSections.other })
  }

  return sections
}

